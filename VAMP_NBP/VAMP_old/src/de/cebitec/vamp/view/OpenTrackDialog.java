    private PersistantTrack[] fillList(){
        List<PersistantTrack> list = refGenConnector.getAssociatedTracks();

        PersistantTrack[] tracks = new PersistantTrack[list.size()];
        for(int i = 0; i < list.size(); i++){
            tracks[i] = list.get(i);
        }

        return tracks;
    }

